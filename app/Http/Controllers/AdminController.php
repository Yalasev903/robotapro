    public function resendGreeting(Request $request)
    {
        $adminSetting = AdminSetting::first();

        if (!$adminSetting || !$adminSetting->auto_greeting_enabled) {
            return back()->with('error', '–ê–≤—Ç–æ–ø—Ä–∏–≤—ñ—Ç–∞–Ω–Ω—è –≤–∏–º–∫–Ω–µ–Ω–æ.');
        }

        $message = $request->input('message') ?: ($adminSetting->auto_greeting_text ?? '–í—ñ—Ç–∞—î–º–æ!');
        $userId = $request->input('user_id');

        if ($userId) {
            $user = User::find($userId);
            if (!$user) return back()->with('error', '–ö–æ—Ä–∏—Å—Ç—É–≤–∞—á–∞ –Ω–µ –∑–Ω–∞–π–¥–µ–Ω–æ.');

            Notification::create([
                'user_id' => $user->id,
                'title' => '–ü—Ä–∏–≤—ñ—Ç–∞–Ω–Ω—è',
                'message' => '–ü—Ä–∏–≤—ñ—Ç ' . $user->name . '! ' . $message,
                'read' => false,
            ]);
        } else {
            foreach (User::all() as $user) {
                Notification::create([
                    'user_id' => $user->id,
                    'title' => '–ü—Ä–∏–≤—ñ—Ç–∞–Ω–Ω—è',
                    'message' => $message,
                    'read' => false,
                ]);
            }
        }

        return back()->with('success', '–ü—Ä–∏–≤—ñ—Ç–∞–Ω–Ω—è –Ω–∞–¥—ñ—Å–ª–∞–Ω–æ.');
    }

